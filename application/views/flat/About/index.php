						<p>ವೆಂಕಟರಮಣ ಶಾಲೆಗೆ ಹೋಗಬೇಕು, ಎಂದರೆ ಆಗಿನ ದಿನಗಳಲ್ಲಿ ಎಂಟಾಣೆ ಫೀ ಕೊಡಬೇಕಾಗಿದ್ದಿತು. ಪ್ರಾಥಮಿಕ ಶಿಕ್ಷಣ ಉಚಿತವಾಗಿರಲಿಲ್ಲ. ತಾಯಿ ಹೇಳಿದರು : "ನಿನಗೆ ಕಲಿಯಬೇಕೆಂದಿದ್ದರೆ ಸಂಸ್ಕೃತ ಕಲಿ. ಇಲ್ಲವೆ ಯಾವುದಾದರೂ ಊರಿಗೆ ಹೋಗಿ ಹೋಟೆಲಿನಲ್ಲಿ ಕೆಲಸ ಹಿಡಿ".</p>
						<p>ಫೀ ಕೊಡಲಾಗದೆ ಒಂದು ದಿನ ಶಾಲೆಯಿಂದ ಹೊರಹಾಕಿಸಿಕೊಂಡು ಅಳುತ್ತ ಬರುತ್ತಿದ್ದಾಗ ಓರ್ವ ಧಾರ್ಮಿಕ ಮಹಿಳೆ ಹೇಳಿದ್ದು : "ಇಕೋ ಎಂಟಾಣೆ. ಮನೆಗೆ ಬಂದು ಪೂಜೆ ಮಾಡು. ಗಂಜಿ ಊಟ ಮಾಡಿ ಶಾಲೆಗೆ ಹೋಗುವಿಯಂತೆ".</p>
						<p>೧೯೨೪- ಹೈಸ್ಕೂಲ್ ಶಿಕ್ಷಣವೂ ಬಡತನದ ಸ್ಥಿತಿಯಲ್ಲಿ. ಅವರ ದೊಡ್ಡಪ್ಪನ ಮಗ ವೆಂಕಟದಾಸಾರ್ಯರ ಸಹಾಯದಿಂದ ಹೈಸ್ಕೂಲ್ ಶಿಕ್ಷಣ, ಕೊನೆಯ ಪರೀಕ್ಷೆಯಲ್ಲಿ ಶಾಲೆಗೆ ಪ್ರಥಮ ಸ್ಥಾನ, ಗಣಿತಕ್ಕೆ ೧೦೦%, ಇಂಗ್ಲೀಷ್‌ಗೆ ೮೫%.</p>
						<p>೧೯೩೧- ಕಾಲೇಜ ಶಿಕ್ಷಣ? ಮಂಗಳೂರಿಗೆ ಹೋಗಬೇಕು. ಹೈಸ್ಕೂಲ್ ಮುಖ್ಯಾಧ್ಯಾಪಕರು ಸೂಚಿಸಿದರು: "ಅಲ್ಲಿಯ ಅಲ್ಯೊಸಿಯಸ್ ಕಾಲೇಜಿನಲ್ಲಿ ಮಾಫಿ ಮಾಡಿಸುವೆ". ಆದರೆ ಊಟಕ್ಕೇನು ಮಾಡಬೇಕು? ಸರಿ, ಅಲ್ಲಿಗೆ ಮುಂದಿನ ಶಿಕ್ಷಣದ ಮುಕ್ತಾಯ.</p>
