						<p>ಪಾವೆಂ ಆಚಾರ್ಯರು 1940ರ ಸುಮಾರು ಹುಬ್ಬಳ್ಳಿಯ ಸಂಯುಕ್ತ ಕರ್ನಾಟಕ ಪತ್ರಿಕಾಲಯಕ್ಕೆ ಸೇರಿದಂದಿನಿಂದ ಅವರ ಪತ್ರಿಕೋದ್ಯಮದ ನಂಟು ಪೂರ್ಣ ಪ್ರಮಾಣದಲ್ಲಿ ಪ್ರಾರಂಭವಾಯಿತೆನ್ನಬಹುದು. ಅವರ ಪ್ರತಿಭೆ ಮತ್ತು ಪಾಂಡಿತ್ಯಗಳನ್ನು ಗುರುತಿಸಿದ ಮೊಹರೆ ಹನುಮಂತರಾಯರು ಆಚಾರ್ಯರನ್ನು ತಡಮಾಡದೇ ಪತ್ರಿಕಾಲಯದ ಸಂಪಾದಕೀಯ ವಿಭಾಗಕ್ಕೆ ಸೆಳೆದು ‘ಕರ್ಮವೀರ’ ವಾರಪತ್ರಿಕೆಯ ಜವಾಬ್ದಾರಿಯುತ ಸ್ಥಾನದಲ್ಲಿ ಕೂರಿಸಿದರು. ಮುಂದೆ 1956ರಲ್ಲಿ ‘ಕಸ್ತೂರಿ’ ಮಾಸಪತ್ರಿಕೆಯ (ಇಂಗ್ಲೀಷ್ ರೀಡರ್ಸ್ ಡೈಜಸ್ಟ  ಮಾದರಿ) ಹೊರಡಿಸುವಾಗ ಪಾವೆಂ ಅವರನ್ನು ಸಂಪಾದಕತ್ವದ ಹೊಣೆ ಹೊರಲು ನಿಯೋಜಿಸಿದರು. ಮುಂದಿನದು ಇತಿಹಾಸ. ಕಸ್ತೂರಿ ‘ಕನ್ನಡಕ್ಕೊಂದೇ ಕಸ್ತೂರಿ’ ಯಾಗಿ ಸುಮಾರು ಎರಡು ದಶಕಗಳಷ್ಟು ಕಾಲ ಕನ್ನಡಿಗರ ಕಣ್ಮಣಿಯಾಗಿ ಇನ್ನಿಲ್ಲದಂತೆ ಮೆರೆಯಿತು. ತನ್ನ ಸಂಪಾದಕೀಯ ಮುಂದಾಳ್ತನದಲ್ಲಿ  ಆಚಾರ್ಯರು ತಾವೇ ವೈವಿಧ್ಯಮಯ ಮೌಲಿಕ ಲೇಖನಗಳನ್ನು ಬರೆದು ಬೇರೆಯವರಿಂದ ಬರೆಸಿ ಕಸ್ತೂರಿಯ ಕಂಪನ್ನು ಕರ್ನಾಟಕದಾದ್ಯಂತ ಪಸರಿಸಿದರು.</p>

						<p>ಆಚಾರ್ಯರ ಬಹುಪಾಲು ಬರಹಗಳು ‘ಲಾಂಗೂಲಾಚಾರ್ಯ’ ಎಂಬ ಹೆಸರಿನಡಿಯಲ್ಲಿ ಮೂಡಿಬಂದ ಸಮಾಜದ ಅಂಕುಡೊಂಕುಗಳನ್ನು ಸಹಾನುಭೂತಿಯಿಂದ ವೀಕ್ಷಿಸಿ ಬರೆದ ವಿದ್ವತ್ಪೂರ್ಣ ಹಾಸ್ಯ ಲೇಪಿತ ಹರಟೆಗಳು. ಇದಲ್ಲದೆ ವೈಜ್ಞಾನಿಕ ,ಐತಿಹಾಸಿಕ, ವೈದ್ಯಕೀಯ, ಆರ್ಥಿಕ, ರಾಜಕೀಯ, ಸಾಮಾಜಿಕ, ಅಂತಾರಾಷ್ಟ್ರೀಯ ವಿದ್ಯಮಾನ, ಸಾಹಿತ್ಯಿಕ, ಭಾಷಾಶಾಸ್ತ್ರಕ್ಕೆ ಸಂಬಂಧಿಸಿದ ವಿಶ್ಲೇಷಣಾತ್ಮಕ, ವೈಚಾರಿಕ ಲೇಖನಗಳು ಪುಂಖಾನುಪುಂಖವಾಗಿ ಅವರ ಲೇಖನಿಯಿಂದ ಹರಿದು ಬಂದಿವೆ.</p>

						<p>ಕಾವ್ಯ ಪಾವೆಂರ ನೆಚ್ಚಿನ ಸಾಹಿತ್ಯ ಪ್ರಕಾರ. ಸಂಖ್ಯೆಯ ದೃಷ್ಟಿಯಿಂದ ಅವರು ಬರೆದ ಕವನಗಳು ಹೆಚ್ಚೆನು ಇಲ್ಲ. ಪಾರಂಪರಿಕ, ನವೋದಯ ಮತ್ತು ನವ್ಯ ಎನ್ನಬಹುದಾದ ಎಲ್ಲ ಕಾವ್ಯ ಪ್ರಕಾರಗಳಲ್ಲೂ ಅವರು ರಚಿಸಿದ ಕವನಗಳು ಸಾರ್ವತ್ರಿಕವಾಗಿ ಶ್ಲಾಘಿಸಲ್ಪಟ್ಟಿವೆ. </p>

						<p>ಶಬ್ದಗಳ ಮೂಲ ಮತ್ತು ವಿಕಸನಕ್ಕೆ ‘ಪದಾರ್ಥ ಚಿಂತಾಮಣಿ’ ಎಂಬ ಶಿರೋನಾಮೆಯಡಿಯಲ್ಲಿ ಆಚಾರ್ಯರು ಬರೆದ ಕುತೂಹಲಕಾರಿ ಕಿರು ಲೇಖನಗಳು ಅವರ ಸಂಶೋಧನಾ ಪ್ರವೃತ್ತಿಗೆ , ಪ್ರತಿಭೆಗೆ, ಪಾಂಡಿತ್ಯಕ್ಕೆ ಕನ್ನಡಿ ಹಿಡಿದಿವೆ. ಎಂಥ ಗಂಭೀರ ವಿಷಯಗಳನ್ನು ಎಷ್ಟು ಸುಲಲಿತವಾಗಿ ,ರಂಜನೀಯವಾಗಿ ಸಾದರ ಪಡಿಸಬಹುದು ಎಂಬುದಕ್ಕೆ ಈ ಲೇಖನಗಳೇ ಸಾಕ್ಷಿ.</p>

						<p>ಪಾವೆಂರವರಿಗೆ ಉಚ್ಚ ಶಿಕ್ಷಣಕ್ಕಾಗಿ ಕಾಲೇಜು ಮೆಟ್ಟಿಲೇರಲು ಸಾಧ್ಯವಾಗದಿದ್ದರೂ ಅವರ ಬರವಣಿಗೆಯಲ್ಲಿ ವ್ಯಕ್ತವಾಗುವ ಪ್ರತಿಭೆ, ಅರಿವಿನ ವಿಸ್ತಾರ, ಸಮಸ್ಯೆಗಳನ್ನು ಪೂರ್ವಾಗ್ರಹವಿಲ್ಲದೇ ವಿಶ್ಲೇಷಿಸುವ, ವಿಮರ್ಷಿಸುವ ಸಾಮಥ್ರ್ಯ, ವೈಚಾರಿಕತೆ ಮುಂತಾದವು ಎಂಥ ಡಾಕ್ಟರೇಟ್ ಪಡೆದ ವಿಶ್ವವಿದ್ಯಾಲಯದ ಪ್ರೊಫೆಸರ್‍ಗಳನ್ನೂ ಬೆರಗುಗೊಳಿಸುವಂಥದ್ದು. ಪತ್ರಿಕಾಲಯದವರ ಸಹವರ್ತಿಗಳು ಪಾವೆಂರವರನ್ನು ‘ಸರ್ವಜ್ಞಾಚಾರ್ಯ’ ರೆಂದೇ ಕರೆಯುತ್ತಿದ್ದರು!</p>

						<p>ತಮ್ಮ ಬಹುಜ್ಞಾನದ ಬಗ್ಗೆ ಆಚಾರ್ಯರು ಎಂದೂ ಬೀಗಿದವರಲ್ಲ. ಜೀವನದುದ್ದಕ್ಕೂ ಸರಳ, ಸಾಮಾನ್ಯ, ಸಂಭಾವಿತ, ಸಾತ್ವಿಕ ವ್ಯಕ್ತಿತ್ವವನ್ನು ಮೈಗೂಡಿಸಿಕೊಂಡಿದ್ದವರು. ಸರಸ್ವತಿಯ ಕೃಪೆ ಅವರ ಮೇಲೆ ಧಾರಾಲವಾಗಿದ್ದರೂ ಧನಲಕ್ಷೀದೇವಿ ಅವರತ್ತ ಸುಳಿಯಲು ಮನಸ್ಸು ಮಾಡಲೇ ಇಲ್ಲ. (ಕೈಹಿಡಿದ ಹೆಂಡತಿಯ ಹೆಸರು ಮಾತ್ರ ಲಕ್ಷೀದೇವಿ) ಪದವಿ ಪ್ರಶಸ್ತಿಗಳಿಗಾಗಿ ಅವರೆಂದೂ ಹಾತೊರೆಯಲಿಲ್ಲ. ಆದರೂ ಜೀವನದ ಸಂಜೆಯಲ್ಲಿ ಹಲವಾರು ಸಾಹಿತ್ಯ ಪ್ರಶಸ್ತಿಗಳು ಅವರನ್ನರಸಿ ಬಂದವು.  ಪತ್ರಿಕೋದ್ಯಮಕ್ಕಾಗಿ ನೀಡಲಾಗುವ ಪ್ರತಿಷ್ಟಿತ ‘ಗೋಯೆಂಕಾ ಪ್ರಶಸ್ತಿ’ (1992) ಸ್ವೀಕರಿಸಿದ ಒಂದು ವಾರದಲ್ಲೇ ಅವರು ನಮ್ಮನ್ನಗಲಿದರು.</p>

						<h3>ಪಾವೆಂ ಗತಿಸಿದ ನಂತರ ಆದ ಬೆಳವಣಿಗೆಗಳು.</h3>
