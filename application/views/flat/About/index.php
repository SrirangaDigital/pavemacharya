								<li>ವಿದ್ಯುನ್ಮಾನ ಮಾಧ್ಯಮದ ಮೂಲಕವೂ ಆಚಾರ್ಯರ ಸರ್ವಸಾಧನೆಗಳನ್ನು ದಾಖಲಿಸಲಾಗಿದೆ.</li>
								<li>ಲೋಕಶಿಕ್ಷಣ ಸಂಸ್ಥೆಯವರ ಅನುಮತಿಯನ್ನು ಪಡೆದು ಕಸ್ತೂರಿ ಸಂಚಿಕೆಗಳನ್ನು ಆರ್ಕೇವ್ ಮಾಡಿ ಇಡಲಾಗಿದೆ</li>
