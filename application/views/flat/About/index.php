						<h4>Literary activity</h4>
						<p>Sri. Sharma was deeply interested in writing, right from his student life. He used to participate enthusiastically in Poetry, Debate and Elocution competition. Being labelled a ‘bookform’ since childhood, Sri. Sharma utilized every moment of his professional life for learning and teaching. Participating in discourses and dramas was his hobby. His contribution to textbook committee is invaluable.</p>
						<p>Sri. Sharma had the opportunity of translating the entire Srimad Valmiki Ramayana to Kannada, under the encouragement of Sri. DVG. This was a stroke of fortune for the people of Karnataka. Sri. Sharma served a feast of Ramakathaa to Kannadigas, translating the text in a style eminently suited to the Kannada language. Sri. Sharma was also the president of the Old Kannada Prose Translation committee of the Kannada Sahitya Parishat, and translated about 13 ancient Kannada texts to modern Kannada.</p>
						<p>Sri. Sharma was constantly communicating his thoughts to the public through regular writings for various daily, weekly and monthly magazines. Leading an extremely fruitful life, he has contributed more than 90 texts to Sanskrit and Kannada literature, including articles, dramas, stotras, translations and much more. He took part in various scholarly meetings across the country, and was constantly engaged in the service of Goddess Sarasvati. He used to take part in assemblies of all mathas, without any distinction of caste or creed.</p>
